/*
    대소 관계 비교 연산자 (Relational Operators)
    > 크다
    < 작다
    >= 크거나 같다
    <= 작거나 같다
*/ 

console.log(2 > 3); // 2가 3보다 크다 false
console.log(2 < 3); // 2가 3보다 작다 true
console.log(3 > 2); // 3이 2보다 크다 true
console.log(3 < 2); // 3이 2보다 작다 false
console.log(3 <= 2); // 3이 2보다 작거나 같다 false
console.log(3 <= 3); // 3이 3보다 작거나 같다 true
console.log(3 >= 3); // 3이 3보다 크거나 같다 true
console.log(3 >= 2); // 3이 2보다 크거나 같다 true