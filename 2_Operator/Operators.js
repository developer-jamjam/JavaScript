/*
    문 Statement : 최소 실행 단위
    선언하면 => 선언문
    할당하면 => 할당문
    반복하면 => 반복문
    조건이있다면 => 조건문

    표현식(Expressions) : 값으로 평가 될 수 있는 문


1; // 숫자 리터럴 표현식
1+1; // 연산자 표현식
call(); //함수호출  표현식
let black; // 선언문
black = 'jin'; //할당문, 할당 표현식인 문

*/