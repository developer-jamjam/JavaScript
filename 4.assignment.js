/*
    할당 연산자 (Assignment Operators)
*/

let a = 1; //할당
a = a+2; //재할당
console.log(a); // 3
a += 2; // a = a + 2; 축약버전
console.log(a); // 5
a -= 2; // a = a - 2; 축약버전
console.log(a); // 3
a *= 2; // a = a * 2; 축약버전
console.log(a); // 6
a /= 2; // a = a / 2; 축약버전
console.log(a); // 3
a %= 2; // a = a % 2; 축약버전
console.log(a); //1
a **= 2; // a = a ** 2; 축약버전
console.log(a); //1